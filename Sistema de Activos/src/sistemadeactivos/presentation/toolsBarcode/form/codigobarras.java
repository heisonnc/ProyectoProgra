    private void ButtonSeeCodeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonSeeCodeActionPerformed
        // TODO add your handling code here:
        Barcode barcode = null;

        try {
            barcode = BarcodeFactory.createCode39(txtcodigo.getText(), true);

        } catch (Exception e) {
        }
        barcode.setDrawingText(true);

        barcode.setBarWidth(2);
        barcode.setBarHeight(60);
        BufferedImage image = new BufferedImage(300, 100, BufferedImage.TYPE_INT_ARGB);
        Graphics2D g = (Graphics2D) image.getGraphics();

        try {
            barcode.draw(g, 5, 20);
        } catch (Exception e) {
        }
        ImageIcon icon = new ImageIcon(image);
        lblcode.setIcon(icon);

    }//GEN-LAST:event_ButtonSeeCodeActionPerformed

