    private void ButtonSafeCodeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonSafeCodeActionPerformed
        // TODO add your handling code here:

        Barcode barcode = null;
        try {
            barcode = BarcodeFactory.createCode39(txtcodigo.getText(), true);
        } catch (Exception e) {
        }
        barcode.setDrawingText(true);

        barcode.setBarWidth(2);
        barcode.setBarHeight(60);
        try {
            
            String ruta=this.safe();

            FileOutputStream fos = new FileOutputStream(ruta+".png");

            try {
                BarcodeImageHandler.writePNG(barcode, fos);
            } catch (OutputException ex) {
                Logger.getLogger(codigobarras.class.getName()).log(Level.SEVERE, null, ex);
            }

        } catch (FileNotFoundException ex) {
            Logger.getLogger(codigobarras.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_ButtonSafeCodeActionPerformed

    private void ButtonPrintActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonPrintActionPerformed
        // TODO add your handling code here:
        Barcode barcode = null;
        try {
            barcode = BarcodeFactory.createCode39(txtcodigo.getText(), true);
        } catch (Exception e) {
        }
        barcode.setDrawingText(false);

        barcode.setBarWidth(2);
        barcode.setBarHeight(60);

        PrinterJob job = PrinterJob.getPrinterJob();
        job.setPrintable(barcode);

        if (job.printDialog()) {
            try {
                job.print();
            } catch (PrinterException ex) {
                Logger.getLogger(codigobarras.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_ButtonPrintActionPerformed

    
    
    public String safe(){
        FileDialog dialogoArchivo;
        dialogoArchivo = new FileDialog(this, "Lista de Archivos desde Frame", FileDialog.SAVE);
        dialogoArchivo.setVisible(true);
        if (dialogoArchivo.getFile() != null) {
            /* Validar que se haya Seleccionado un Archivo*/
            String directorio = dialogoArchivo.getDirectory();
            String nombreArchivo = dialogoArchivo.getFile();
            String rutatotal = directorio + nombreArchivo;
           return rutatotal;
        } else {
            System.out.println("No Seleccionó Archivo");
            return " ";
        }
        
    }
